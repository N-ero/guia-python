#Una variable es como una cajita donde vamos a guardar diferentes tipos de datos.
#Por ejemplo. Vamos a nombrar una variable que almacena nuestro nombre completo, luego la vamos a mostrar en pantalla.
#NOTA: Recuerda que un nombre está compuesto por caracteres de texto, lo que es un tipo de dato string. 

nombre = "Cristopher Méndez Cervantes"
print(nombre)
#Como podemos ver, hemos designado una variable llamada nombre con el dato string "Cristopher Méndez Cervantes"
#Podemos usar la función print y dentro de ella hacer un llamado a la variable nombre, que a su vez hace un llamado a los datos que contiene. 
#De tal manera que este código muestra en pantalla el contenido de la variable nombre. 

#También podemos emplear variables para almacenar datos de tipo numérico. Recordemos que hay 2 tipos de datos numéricos. 
#Vamos a nombrar dos variables que almacenen dos números distintos y luego vamos a operarlos aritméticamente:
x = float(1)
y = float(20)
print(x-y)

#Como podemos ver, hemos designado dos variables indicando que son números float (a modo de ejemplo) y se operan con la función print. 

#También sirve para operadores lógicos. Veamos el siguiente ejemplo:
verdad = 3==3
print(verdad)
#3 es igual a 3, es true. 